import static java.util.stream.Collectors.toList;

@Entity
@Table(name = "users")
public class UsersDao {
    @Id
    @Column(name = "user_id")
    @GeneratedValue
    private Long id;

    @Column(name = "email")
    private String email;

    @Column(name = "username")
    private String username;

    @Column(name = "password")
    private String password;

}
